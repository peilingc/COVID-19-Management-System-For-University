    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel)wardJTable.getModel();
        model.setRowCount(0);  
        for(Ward w : wardDirectory.getWardList()){
            Object row[] = new Object[4];
            row[0] = w;
            row[1] = w.getDepartment();
            row[2] = w.getCapacity(); 
            row[3] = w.getRemainingBeds();
            model.addRow(row);
        }
    }

    private void refreshTable(int wardID) {
        DefaultTableModel model = (DefaultTableModel)wardJTable.getModel();
        model.setRowCount(0);
        for(Ward w : wardDirectory.getWardList()){
            if(w.getWardID() == wardID){
                Object row[] = new Object[4];
                row[0] = w;
                row[1] = w.getDepartment();
                row[2] = w.getCapacity(); 
                row[3] = w.getRemainingBeds();                   
                model.addRow(row);
            }
        }
    }
