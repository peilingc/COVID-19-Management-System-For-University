    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if (txtCap.getText().length()==0 || txtRemain.getText().length()==0 
            || txtDep.getText().length()==0 || txtID.getText().length()==0)
        {
            JOptionPane.showMessageDialog(null, "Please ensure all fields are entered!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try{
            int ID = Integer.parseInt(txtID.getText());
            int cap = Integer.parseInt(txtCap.getText());
            int remain = Integer.parseInt(txtRemain.getText());
            String dep = txtDep.getText();
            
            Ward w = wardDirectory.addWard();
            w.setDepartment(dep);
            w.setCapacity(cap);
            w.setRemainingBeds(remain);
            w.setWardID(ID);
            
            txtID.setEnabled(false);
            txtDep.setEnabled(false);
            txtCap.setEnabled(false);
            txtRemain.setEnabled(false);
            btnSubmit.setEnabled(false);
            btnUpdate.setEnabled(false);
            populateTable();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please enter only numbers for ID, Capacity and Remaining Beds!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

